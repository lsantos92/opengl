#version 330 core
out vec4 FragColor;

in vec3 FragPos;
in vec3 Normal;
in vec3 LightPos;   // extra in variable, since we need the light position in view space we calculate this in the vertex shader

uniform vec3 lightColor;
uniform vec3 objectColor;

void main()
{
    // ambient
    float ambientStrength = 0.1;
    vec3 ambient = ambientStrength * lightColor;
    
     // diffuse
    vec3 norm = normalize(Normal);
    vec3 lightDir = normalize(LightPos - FragPos);
    float diff = max(dot(norm, lightDir), 0.0);
    vec3 diffuse = diff * lightColor;
    
    // specular
    float specularStrength = 0.5;
    vec3 viewDir = normalize(-FragPos); // the viewer is always at (0,0,0) in view-space, so viewDir is (0,0,0) - Position => -Position
    vec3 reflectDir = reflect(-lightDir, norm);
    float spec = pow(max(dot(viewDir, reflectDir), 0.0), 32); //specular component ->  calculate the dot product between the view direction and the reflect direction
    vec3 specular = specularStrength * spec * lightColor;                           // (and make sure it's not negative) and then raise it to the power of 32.
                                                                                    // This 32 value is the shininess value of the highlight. The higher the shininess value of an object,
    vec3 result = (ambient + diffuse + specular) * objectColor;                     // the more it properly reflects the light instead of scattering it all around and thus the smaller the highlight becomes.
    FragColor = vec4(result, 1.0);
    
   
}

